Decidem as partes, na melhor forma de direito, celebrar o presente CONTRATO DE PRESTAÇÃO DE SERVIÇOS DE PROVEDOR DE INTERNET, que reger-se-á nos termos da Lei Geral de Telecomunicações, n.º 9.472/1997 e mediante as cláusulas e condições adiante estipuladas.

CLÁUSULA PRIMEIRA - DO OBJETO DO CONTRATO

1.1 A CONTRATADA é responsável pela disposição do CONTRATANTE, o serviço individual de provedor de internet na velocidade escolhida pelo CONTRATANTE, durante o tempo de vigência do contrato.

1.2 O objeto também compreende o Serviços de Comunicação Multimídia (SCM), que compreendem a disponibilização de rede de transporte para a transmissão de Informações Multimídia, tais como sinais de áudio, vídeo, dados, voz e outros sons.

1.3 A CONTRATADA tem como garantir 10% de banda no acesso. Este percentual leva em conta vários fatores que influenciam na velocidade, tais como região, horário de acesso, demanda, página acessada, configuração de computador, saturação da rede, dentre outros.

1.4 É permitido ao CONTRATANTE, desde que haja viabilidade técnica, a migração da velocidade pela qual optou no ato da adesão, mediante prévia solicitação e reajuste dos preços.

CLÁUSULA SEGUNDA - DAS OBRIGAÇÕES DA CONTRATADA

2.1 Compete à CONTRATADA Providenciar toda mão de obra e obras necessárias à disponibilização das condições físicas do imóvel à instalação do sistema de Internet, arcando com todos os custos dela decorrentes.

2.2 A CONTRATADA deverá disponibilizar suporte remoto 24horas por telefone ou online, de forma a auxiliar o CONTRATANTE em qualquer interrupção do serviço, seja por falha no sistema ou por desconhecimento de manuseio do cliente.

2.3 Se houver necessidade de atendimento local, a empresa tem o prazo de até 72 horas para estar atendendo a solicitação, sem custo ao CONTRATANTE se tratar-se de falha técnica. Tratando-se de falha na observância às orientações por parte do CONTRATANTE, será cobrada uma taxa conforme TERMO DE CONTRATAÇÃO em anexo.

2.4 A CONTRATADA poderá fazer manutenção em seus sistemas sem aviso prévio, ficando totalmente isenta de qualquer responsabilidade por perdas e danos por parte da CONTRATANTE.

2.5 A CONTRATADA igualmente não se responsabiliza por qualquer transação comercial efetuada on-line, as quais serão de inteira responsabilidade de quem realiza a compra ou disponibiliza os produtos ou serviços.

2.6 A CONTRATADA não se responsabiliza por eventuais interrupções ou instabilidades nos sistemas responsáveis por prover o acesso físico do CONTRATANTE à CONTRATADA e/ou desta para com a Internet.

CLÁUSULA TERCEIRA - DAS OBRIGAÇÕES DA CONTRATANTE

3.1 Cabe ao CONTRATANTE o pagamento em dia dos valores pactuados, conforme banda contratada.

3.2 A CONTRATANTE é responsável em obter junto ao síndico do condomínio ou dos demais condôminos, sempre que necessário, a autorização para ligação dos sinais e para realização das obras referidas.

3.3 O CONTRATANTE será responsável pela guarda de seu acesso, por meio de nome/login e sua senha, devendo tomar todas as medidas necessárias para IMPEDIR a utilização de sua senha por QUAISQUER terceiros.

3.4 O CONTRATANTE será responsável por manter as configurações do computador para acesso aos serviços contratados, nos termos orientados pela equipe de instalação, sendo proibido alterar endereços de máquinas, protocolos, IP (Internet Protocol) de rede ou de correio eletrônico, e utilizar técnicas furtivas denominadas "spoofing", na tentativa de responsabilizar terceiros ou ocultar a identidade ou autoria.

3.5 Cabe ao CONTRATANTE a devolução, ao final do contrato, dos equipamentos utilizados na instalação em comodato. No caso dos equipamentos serem danificados, extraviados ou furtados, o CONTRATANTE terá a obrigação de restituí-los em estado novo à empresa.

3.6 Cabe ao CONTRATANTE a total observância à legislação vigente, não cabendo qualquer responsabilização à CONTRATADA se o usuário desrespeitar quaisquer norma vigente bem como dos direitos autorais e de propriedade intelectual.

CLÁUSULA QUARTA - DO VALORES

4.1 Pela instalação e direito de acesso à internet, o CONTRATANTE pagará a CONTRATADA o preço ajustado no TERMO DE CONTRATAÇÃO, nas condições nele indicadas, sendo que se for necessário fazer medição de sinal será cobrado uma taxa de deslocamento de funcionário para tal serviço, valor esse que em caso de indisponibilidade de sinal não será devolvido e em caso de fechamento de contrato esse valor será considerado a tarifa de habilitação.

4.2 A mensalidade deve ser paga na data de vencimento indicadas no TERMO DE CONTRATAÇÃO.

4.3 A mensalidade será reajustada a cada doze meses com base na variação da IGPM, ou, no caso de sua extinção ou da inexistência de sua divulgação, por outro índice que melhor reflita a perda do poder aquisitivo da moeda nacional ocorrida no período.

4.4 A mensalidade só será cobrada à partir do mês seguinte ao da instalação, na data do vencimento estabelecido em contrato.

4.5 O atraso no pagamento, acarretará a cobrança de multa de 4% e juros de mora de 0,06% ao dia, calculado sobre o débito atualizado.

4.6 O não pagamento ocasionará a interrupção dos serviços, no atraso a 15 (quinze), podendo ser protestado em cartório, e seus dados incluídos nos órgão de proteção ao crédito.

4.7 9. Nos preços contratados estão inclusos todos os tributos incidentes e demais encargos específicos do Setor de Telecomunicações, vigentes na data base.

CLÁUSULA QUINTA - DA VIGÊNCIA DO CONTRATO

5.1 A vigência do presente contrato é de 12 meses, sendo renovado automaticamente após esse período caso não haja solicitação de rescisão.

CLÁUSULA SEXTA - DA INSTALAÇÃO

6.1 O prazo de instalação do serviço de internet é de até 15 dias úteis, podendo sofrer alterações, nas seguintes hipóteses: cliente não disponibilize local e/ou computadores adequados para a ativação, força maior, como instabilidade climática; atrasos decorrentes de culpabilidade de terceiros, como atraso na entrega de equipamentos necessários, outras hipóteses que não exista culpabilidade da CONTRATADA;

CLÁUSULA SÉTIMA - DA RESCISÃO

7.1 O CONTRATANTE pode solicita à CONTRATADA a rescisão do contrato, com antecedência mínima de 30 dias por escrito devendo durante esse período cumprir integralmente com as obrigações estabelecidas neste contrato.

7.2 No caso de rescisão contratual dentro do período de vigência (12 meses), será devido o valor de instalação, desinstalação e a análise do equipamento por possíveis danos causados, conforme preço do TERMO DE CONTRATAÇÃO.

CLÁUSULA OITAVA - DAS DISPOSIÇÕES GERAIS

8.1 O CONTRATANTE, neste ato, autoriza expressamente a CONTRATADA a enviar ao CONTRATANTE, emails, malas diretas, encartes ou qualquer outro instrumento de comunicação ofertando serviços e/ou promoções da CONTRATADA. Tais permissões podem ser revogadas pelo CONTRATANTE a qualquer momento através de solicitação feita por meio da Central de Relacionamento.

8.2 As partes elegem o foro da cidade depara conhecer das questões porventura emergentes da presente relação contratual, com a expressa renúncia de qualquer outro, por mais privilegiado que possa vir a ser.

